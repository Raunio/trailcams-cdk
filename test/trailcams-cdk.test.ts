import * as cdk from "aws-cdk-lib";
import { Template } from "aws-cdk-lib/assertions";
import * as TrailcamsCdk from "../lib/trailcams-cdk-stack";

// example test. To run these tests, uncomment this file along with the
// example resource in lib/trailcams-cdk-stack.ts
test("Email attachment lambda handler created", () => {
  const app = new cdk.App();
  // WHEN
  const stack = new TrailcamsCdk.TrailcamsCdkStack(app, "MyTestStack");
  // THEN
  const template = Template.fromStack(stack);
  template.hasResourceProperties("AWS::SQS::Queue", {
    VisibilityTimeout: 300,
  });
});
